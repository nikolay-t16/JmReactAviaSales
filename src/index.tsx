import reducer from './store/reducer';
import App from './App';
import reportWebVitals from './reportWebVitals';

const composeEnhancers =
  typeof window === 'object' &&
  // @ts-ignore
  window.__REDUX_DEVTOOLS_EXTENSION_COMPOSE__
